package ma.ehei.model;

public class Etudiant {

    String id;

    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    String Nom;
    public String getNom() {
        return Nom;
    }
    public void setNom(String nom) {
        Nom = nom;
    }

    String Prenom;

    public String getPrenom() {
        return Prenom;
    }
    public void setPrenom(String prenom) {
        Prenom = prenom;
    }

}
